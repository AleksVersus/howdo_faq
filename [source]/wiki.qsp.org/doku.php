<p>
Данный цикл статей - новая версия справки по QSP. Здесь будет собрано всё необходимое для освоения платформы.
</p>

<p>
Обратная связь принимается на форуме: <a href="http://qsp.su/index.php?option=com_agora&amp;task=topic&amp;id=647&amp;Itemid=57" class="urlextern" title="http://qsp.su/index.php?option=com_agora&amp;task=topic&amp;id=647&amp;Itemid=57"  rel="nofollow">Новая справка по QSP</a>.
</p>

<p>
<em class="u"><em>Есть желание</em></em> приобщиться к текстовым играм, но пока <em class="u"><em>нет идеи</em></em>? Скачайте &quot;Набор игрока&quot; с <a href="http://qsp.su/" class="urlextern" title="http://qsp.su/"  rel="nofollow">главной страницы сайта</a> и просто поиграйте, чтобы проникнуться идеей таких игр.
</p>

<p>
<em class="u"><em>Есть желание</em></em> начать писать текстовые игры, даже <em class="u"><em>есть идея</em></em>, но <em class="u"><em>нет опыта</em></em> программирования? Смело открывайте <a href="http://qsp.su/index.php?option=com_content&amp;task=view&amp;id=59&amp;Itemid=56" class="urlextern" title="http://qsp.su/index.php?option=com_content&amp;task=view&amp;id=59&amp;Itemid=56"  rel="nofollow">учебник</a>, который специально создан для авторов, не имеющих опыта в программировании.
</p>

<p>
Ну а остальных милости просим в эту справку.
</p>
